// Function to find the best matching response
export const getCareerResponse = (userMessage) => {
  const message = userMessage.toLowerCase().trim();
  
  // Check each category for matching questions
  for (const [category, data] of Object.entries(careerResponses)) {
    for (const question of data.questions) {
      if (message.includes(question.toLowerCase())) {
        return data.response;
      }
    }
  }
  
  // Check for keywords and provide relevant response
  if (message.includes('10th') || message.includes('tenth')) {
    return careerResponses.after10th.response;
  }
  
  if (message.includes('engineering') || message.includes('engineer')) {
    return careerResponses.engineering.response;
  }
  
  if (message.includes('medical') || message.includes('doctor') || message.includes('mbbs')) {
    return careerResponses.medical.response;
  }
  
  if (message.includes('commerce') || message.includes('business') || message.includes('ca') || message.includes('mba')) {
    return careerResponses.commerce.response;
  }
  
  if (message.includes('arts') || message.includes('humanities') || message.includes('psychology') || message.includes('law')) {
    return careerResponses.arts.response;
  }
  
  if (message.includes('skill') || message.includes('learn') || message.includes('develop')) {
    return careerResponses.skills.response;
  }
  
  // Default response for general career guidance
  return careerResponses.general.response;
};

